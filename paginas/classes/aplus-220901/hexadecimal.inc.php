<h3>Hexadecimal</h3>

<p>Hexadecimal is a <b>base-16</b> value system that uses the numbers 0 to 9 and the alphabetical letter A to F to represent values.
 The base values of hexadecimals are as listed below.</p>
  
<p>A prime example of hexadecimal values are color representations. The color white is represented as FFFFFF and the color black is represented as 000000.
Another good example of hexadecimal usage is the newer IPv6.</p>

