<h3>Motherboard Form Factors</h3>

<p class="pheader">Overview</p>

<p>ATX stands for Advanced Technology Extended. It was standardized by Intel in 1995. 
The ATX form factor has been updated many times over the years. It has a 20 or 24 pin power connector.</p>

<p>A smaller standard is the ITX form factor. This technology was developed by VIA Technologies in 2001.</p>

