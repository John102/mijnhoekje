<p>LCD Displays</p>

<p>LCD displays work at the molecular level where crystals are being twisted to shine light through polarized filters. To be able to see light the LCD display needs backlight. Older LCD displays used fluorescent backlighting (CCFL - Cold Cathode Fluorescent Lamp) for this purpose. 
More modern LCD displays use LED backlights. So a LED display is actually a LCD display with LED backlight.</p>

