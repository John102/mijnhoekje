<h3>Displays</h3>

<p class="pheader">Overview</p>

<p>Back in the early days personal computers used CRT (Cathode Ray Tube) monitors. These monitors were big and heavy and used a vacuum tube that contained one or more electron guns and a phosphorescent screen to display images.</p>
<p>Nowadays the monitor market has been conquered by the LCD (Liquid Crystal Display) screens. These LCD screens are a lot smaller (flat screen) and are way more energy efficient.</p>
<p>When buying a LCD monitor there are several properties that you have to take into consideration.</p>

