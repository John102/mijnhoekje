<p>We will generally see 2 different LCD technologies out there but some older technologies are still around. The technologies and their respective properties are listed in the table below.</p>

<table>
	<tr>
		<th>LCD Technology</th>
		<th>Properties</th>
	</tr>
	<tr>
		<td>TN (Twisted Nematic)</td>
		<td>
			<ul class="magiclist">
				<li>Most common</li>
				<li>Fasted response times</li>
				<li>Less power consumption</li>
				<li>Poor viewing angles</li>
				<li>Worse color representation</li>
			</ul>
		</td>
	</tr>
	<tr>
		<td>IPS (In Plain Switching)</td>
		<td>
			<ul class="magiclist">
				<li>Excellent color representation</li>
				<li>Good viewing angle</li>
				<li>No tailing when touched (mobile devices)</li>
				<li>More power consumption</li>
				<li>More expensive</li>
			</ul>
		</td>
	</tr>
	<tr>
		<td>Plasma</td>
		<td>
			<ul class="magiclist">
				<li>Contains tiny cells with noble gas and mercury</li>
				<li>Mercury sheds energy as UV light</li>
				<li>UV light strikes colored phosphor</li>
				<li>Very good color representation (deep blacks)</li>
				<li>Fast response time</li>
				<li>No longer in production (LED display prices have won the market)</li>
			</ul>
		</td>
	</tr>
	<tr>
		<td>OLED (Organic Light Emitting Diode)</td>
		<td>
			<ul class="magiclist">
				<li>Organic compound that emits light when receiving electric current</li>
				<li>No backlight</li>
				<li>Shorter lifespan (organic materials degrade over time)</li>
				<li>Thinner and lighter screens (no glass needed)</li>
				<li>True black color</li>
			</ul>
		</td>
	</tr>
</table>





