<h3>Processor Sockets</h3>

<p class="pheader">Overview</p>

<p>Processor sockets come in different packet types. The <b>ZIF</b> or '<i>Zero Insertion Force</i>' packet allows the processor microchip to be inserted into the motherboards socket without any force. The Processor is then secured in place by a lever. <br />
The <b>PGA</b> or '<i>Pin Grid Array</i>' is a socket with tiny connector plates inside the socket on the motherboard that come in contact with the pins on the underside of the processor. <br />
The <b>LGA</b> or '<i>Land Grid Array</i>' is a socket that contains all the small fragile pins on the motherboard instead of the processor. This reduces the chance that pins get damaged when handling the processor.</p>

