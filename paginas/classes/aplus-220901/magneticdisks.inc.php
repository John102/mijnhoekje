<h3>Magnetic Drives</h3>
		
		<p class="pheader">Overview</p>
		
			<table>
				<tr>
					<th>Components</th>
					<th>Subscription</th>
				</tr>
				<tr><td>Platter</td><td>Metal or plastic disk coated with magnetic material</td></tr>
				<tr><td>Spindle</td><td>Axis or central hub on which the platters spin around.</td></tr>
				<tr><td>Read/Write heads</td><td>Magnetic part constructed on the actuator arm which reads and writes data on the platters.</td></tr>
				<tr><td>Voice Coil actuator</td><td>Mechanism that moves Read/Write heads very precisely over the tracks on the platters.</td></tr>
			</table>
	
		<p class="pheader">Drive Geometry</p>
		
			
			<table>
				<tr>
					<th>Geometric Category</th>
					<th>Subscription</th>
				</tr>
				<tr><td>Sector</td><td>Smallest writable part on a platter which typically holds 512 bytes of data.</td></tr>
				<tr><td>Cluster</td><td>A logical grouping of 4 to 64 sectors and is smallest writable part for the operating system.</td></tr>
				<tr><td>Track</td><td>Concentric circle written on the platters.</td></tr>
				<tr><td>Cylinder</td><td>Collection of all tracks on all platters at given distance from the spindle.</td></tr>
			</table>

	
		<p class="pheader">Drive Speeds</p>
		
		<p>Mechanical drive platters spin at high rates of speed (<i>RPM -> Revolutions Per Minute</i>). Higher rates equal lower latency (<i>less delay</i>).</p>
		<p>Common rates are 5400 RPM, 7200 RPM. Uncommon high end disk rates are 10.000 RPM, 15.000 RPM.</p>