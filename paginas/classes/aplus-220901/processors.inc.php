<h3>Characteristics</h3>

<p class="pheader">Overview</p>

<p><b>Processor clock speed </b> determines the frequency that the particular processor runs at. Up until not too long ago the speed was expressed in MHz (<i>Megahertz</i>) but as technology progressed so did processor speed capabilities. 
Nowadays processors run at several GHz (<i>Gigahertz</i>) frequency. <i><u><a target="_self" href="main.php?pagina=notitie&files=aplus-220901&file=hertzexplained.inc.php#content">Click here for more information about frequency</a></u></i>.</p>

<p>The actual performance of a processor does not solely depend on the clockspeed though. The processor architecture, bus speed, bus width, L1 and L2 cache sizes and operating system abilities are all involved factors when it comes down to performance. </p>

<p class="pheader">Cache Memory</p>

<p>Processors contain a certain amount of cache memory (<i>L1, L2, L3</i>), which is very fast memory. This cache memory is used to hold data that the processor needs to access efficiently to drastically increase performance.</p>

<p class="pheader">Hyper-Threading Technology (HTT)</p>

<p>HTT is a technology that allows a single physical processor core to act as if it were 2 cores. This results in a 15% to 30% performance increase of that core. HTT is not supported by all operating systems. However Windows XP and newer do understand HTT very well.</p>

<p class="pheader">Virtualization Support</p>

<p>Virtualization is a concept that allows multiple operating systems to be ran on a single hardware system. Both Intel and AMD have added virtualization support to the processor.<br />
Intel Virtualization Technology (VT) software is developed for Intel processors and AMD Virtualization (AMD-V) is built for AMD processors.</p>

<p class="pheader">Integrated Security (No-eXecute bit or <i>NX Bit</i>)</p>

<p>Processors nowadays have integrated security. This technology allows the processor to designate sections of memory where code might be executed and it will designate other sections of memory as protected sections where code cannot run.
 This is especially useful to prevent malware and viruses from executing.<br/>
 Intel calls this technology the XD bit (<i>eXecute Disable</i>) while AMD calls it Enhanced Virus Protection. In order to utilize this NX bit capability the operating system must support the feature. Windows supports this feature under the name of Data Execution Prevention (DEP).</p>