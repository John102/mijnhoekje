<h3>Wireless Encryption</h3>

<p class="pheader">Overview</p>

<p>Wireless communication between devices happen on radio frequencies and this means that basically anyone with proper equipment can tap into these communications.
In order to decrease the chance of data being intercepted and read by uninvited guests there are several ways to encrypt the data.</p>

<table>
	<tr>
		<th>Encryption Type</th>
		<th>Properties</th>
	</tr>
	<tr>
		<td>WEP (Wireless Equivalent Privacy)</td>
		<td>
			<ul class="magiclist">
				<li>64-bit or 128-bit key size</li>
				<li>Cryptographic vulnerabilities found in 2001</li>
				<li>No longer considered safe</li>
			</ul>
		</td>
	</tr>
	<tr>
		<td>WPA (Wi-Fi Protected Access)</td>
		<td>
			<ul class="magiclist">
				<li>Short-term workaround after WEP</li>
				<li>Every packet gets a unique encryption key</li>
				<li>Used RC4 as a TKIP (Temporal Key Integrity Protocol)</li>
				<li>TKIP has its own vulnerabilities</li>
			</ul>
		</td>
	</tr>
	<tr>
		<td>WPA2 (Wi-Fi Protected Access version 2)</td>
		<td>
			<ul class="magiclist">
				<li>Uses AES (Advanced Encryption Standard)</li>
				<li>CCMP (Counter Mode with Cipher Block Chaining Message Authentication Code Protocol) replaced TKIP</li>
				<li>This encryption protocol is still used today</li>
			</ul>
		</td>
	</tr>
</table>