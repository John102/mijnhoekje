<h3>WiFi or 802.11 standard</h3>

<p class="pheader">Overview</p>

<p>Wireless networking is referred to as <i>IEEE 802.11</i>. The IEEE acronym stands for <i>Institute of Electrical and Electronics Engineers</i>. 
There are several major releases of 802.11 (wireless) standards as listed below.</p>

<table>
	<tr>
		<th>Standard</th>
		<th>Maximum Speed</th>
		<th>Frequency</th>
		<th>Description</th>
	</tr>
	<tr>		<td>802.11a</td>	<td>54 Mbit/s</td>	<td>5 GHz</td>		<td>One of the original standards (october 1999)<br/> Smaller frequency range than 802.11b because higher frequency is absorbed by other objects. Many rules-of-thumb calculate 1/3rd of range of 802.11b or 802.11g.<br/>Today only seen in very specific use cases.</td> 	</tr>
	<tr>		<td>802.11b</td>	<td>11 Mbit/s</td>	<td>2.4 GHz</td>	<td>Also original standard (october 1999)<br/> Better range than 802.11a but more frequency conflicts (<i>baby monitors, cordless phones, microwave ovens, Bluetooth</i>. </td> 	</tr>
	<tr>		<td>802.11g</td>	<td>54 Mbit/s</td>	<td>2.4 GHz</td>	<td>Upgrade from 802.11b (june 2003)<br/> Same speed as 802.11a and same range as 802.11b due to low frequency.<br/> 802.11g is backwards compatible with 802.11b enabled devices.</td> 	</tr>
	<tr>		<td>802.11n</td>	<td>600 Mbit/s</td>	<td>2.4 and/or 5 GHz</td>	<td>Upgrade solution to all older standards (October 2009) <br />Using MIMO (<i>multiple-input multiple-output</i>) it is able to run 4 streams of 150 Mbit/s simultaneously.</td> 	</tr>
	<tr>		<td>802.11ac</td>	<td>7 Gbit/s</td>	<td>5 GHz</td>	<td>Upgrade to 802.11n (january 2014)<br/> Larger bandwidth usage, faster data transfers due to denser signaling modulation<br/> 8 MIMO streams</td> 	</tr>
	</tr>

</table>


<p class="pheader">Infrared (IR)</p>

<ul class="magiclist">
	<li>standard from the IrDA (Infrared Data Association)</li>
	<li>4 MBit/s</li>
	<li>1 meter distance limitation</li>
	<li>Has to be within line of sight</li>
	<li>Some laptops, cameras and phones are equipped with IR</li>
</ul>




<p class="pheader">NFC (Near Field Communication)</p>

<ul class="magiclist">
	<li>106 kbit/s, 212 kbit/s, 424 kbit/s</li>
	<li>0 - 10 cm range</li>
	<li>Mobile devices, payment devices, ticketing, gaming</li>
</ul>




