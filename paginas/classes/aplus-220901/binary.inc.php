<h3>Binary</h3>

<p class="pheader">Overview</p>

<p>Binary is a <b>base-2</b> value system that uses the numbers 0 and 1 to represent values.
 To calculate certain values we can use a binary diagram as shown here.</p>

<table>
	<tr>
		<th>128</th><th>64</th><th>32</th><th>16</th>
		<th>8</th><th>4</th><th>2</th><th>1</th>
	</tr>
	<tr>
		<td>1</td><td>1</td><td>1</td><td>1</td>
		<td>1</td><td>1</td><td>1</td><td>1</td>
	</tr>
</table>

<p>The diagram above shows a binary value of 1111 1111 which is equal to the decimal value 255. 
To calculate this decimal value simple add up all the 'enabled' bits (1)s which in this case is all of them.
<b>So, 128 + 64 + 32 + 16 + 8 + 4 + 2 + 1 = 255</b>.<br/>
Let us try one more.</p>

<table>
	<tr>
		<th>128</th><th>64</th><th>32</th><th>16</th>
		<th>8</th><th>4</th><th>2</th><th>1</th>
	</tr>
	<tr>
		<td>1</td><td>1</td><td>0</td><td>1</td>
		<td>0</td><td>0</td><td>1</td><td>1</td>
	</tr>
</table>

<p>The binary value above is equal to 1101 0011 and is calculated as <b>128 + 64 + 16 + 2 + 1 = 211</b>. </p>



