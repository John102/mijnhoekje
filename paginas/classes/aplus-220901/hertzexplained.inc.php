<h3>Hertz Explained</h3>

<p class="pheader">Overview</p>

<p>When we speak of Hertz in digital terms we refer to the amount of state changes per second within the transistors also known as <b>clockcycles per second</b>.</p>
<p>The image below shows a frequency of 1 Hertz. (<i>1 Clockcycle per second</i>)</p>

<img class="image-block" src="<?=$img_misc?>hertz_one.png" />

<p>Increased frequency basically means more state changes or waves in a second.</p>
