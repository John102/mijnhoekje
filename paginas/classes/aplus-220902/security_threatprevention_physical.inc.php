<h3>Physical Threat Prevention</h3>

<p class="pheader">Overview</p>

<p>All computer systems and networks are exposed to certain threats. These threats come in many different forms and are preventable most of the time. 
Threats may vary from physical threats (i.e; vandalism) to digital threats (i.e; viruses).</p>

<p class="pheader">Pyshical Threat Prevention</p>

<table>
	<tr>
		<th>Method</th>
		<th>Explanation</th>
	</tr>	
	
	<tr>
		<td>physical access restrictions</td>
		<td>
			<ul class="magiclist">
				<li>lock server rooms with key or key card locks</li>
				<li>lock server cases</li>
				<li>use cameras or motion sensors to monitor during off hours</li>
				<li>security guard for extremely sensitive systems</li>
			</ul>
		</td>
	</tr>
	
	<tr>
		<td>door access control</td>
		<td>
			<ul class="magiclist">
				<li>mantrap (to prevent tailgating)</li>
				<li>locked doors</li>
				<li>video surveillance</li>
				<li>ID badges and key FOBs</li>
				<li>Biometrics (fingerprint scan, facial recognition, Iris scan)</li>
				<li>RFID badges</li>
			</ul>
		</td>
	</tr>
	
	<tr>
		<td>additional physical controls</td>
		<td>
			<ul class="magiclist">
				<li>cable locks (locks laptop / desktop to an 'anchor' like a heavy table)</li>
				<li>securing physical documents / passwords (locked away)</li>
				<li>disposing physical documents properly (i.e; shredding disposable employerlists)</li>
				<li>smart cards (creditcard sized, holds certificate)</li>
				<li>tokens (authentication code that changes continuously)</li>
				<li>privacy filters (applied screen (mostly laptops) to eliminate screen visibility at an angle a.k.a. shouldersurfing)</li>
				<li>entry control roster (a sort of guestlist or visitor log)</li>
			</ul>
		</td>
	</tr>
	
</table>

<p class="pheader">Recap</p>

<p>The biggest thing to consider is the potential impact of the compromise of the system that is to be secured.<br/>
Along with the likelihood that this will actually occur and the available budget, the security engineer has to come up with a solution. <br/>
So...

<ul class="magiclist">
	<li>impact of compromise</li>
	<li>likelihood of compromise</li>
	<li>budget</li>
</ul>
</p>


<p class="pheader">"Defense in depth" model</p>

<p>A layered solution that deploys security tactics at various levels.

	<ul class="magiclist">
		<li>physical security of systems</li>
		<li>firewall protection on Internet and LAN connections</li>
		<li>intrusion detection software</li>
		<li>anti-virus and anti-spyware protection</li>
		<li>auditing for security breaches</li>
		<li>access control lists and data encryption</li>
	</ul>

</p>




