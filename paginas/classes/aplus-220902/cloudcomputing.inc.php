<h3>Cloud Computing</h3>

<p class="pheader">Overview</p>

<p>Cloud computing is a model for enabling ubiquitous, convenient and on-demand network access to a shared pool of configurable computing resources such as 
<i>networks, servers, storage, applications and services</i>, that you can rapidly provision and release with a minimal management effort or service provider interaction.<br/>
The list belows sums up some advantages of cloud computing.

	<ul class="magiclist">
		<li>virtualized datacenter</li>
		<li>reduction in operational costs</li>
		<li>more energy conscious</li>
		<li>rapid flexability</li>
		<li>on-demand services</li>
		<li>resource pooling</li>
		<li>custom measured service</li>
	</ul>

</p>

