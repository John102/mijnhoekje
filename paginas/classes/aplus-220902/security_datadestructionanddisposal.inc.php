<h3>Security: Data Destruction and Disposal Methods</h3>

<img class="image-block" src="<?=$class_220902.'multimedia/'?>hddshred.png" />

<p class="pheader">Overview</p>

<p>It is of great importance to destroy data and dispose of the hardware or documents properly when they are no longer necessary. 
The disposable data might turn out to be very sensitive and can be used to the benefits of a blackhat hacker or thief.  
Lists with employee record information might grant a hacker physical access to a company by identity theft. Especially in huge companies where 
the security guard does not know all the faces of employees and works with a guest entry roster. 
Hard disks with corporate data that seem broken and rendered useless might still be fixable and/or partially readable. 
If not destroyed and disposed of properly, it might fall into the wrong hands. 
There are many ways that data and documents can fall into the wrong hands and benefit in dark schemes. 
Luckily there are proper ways of disposing and destruction and therefor it is important that an organization has a appropriate data destruction / disposal plan.</p>

<p class="pheader">Hard Drives</p>

<p>There are 3 key concepts in relation to the sensitive data contained on hard drives.</p>
	
	<table>
		<tr>
			<th>Concept</th>
			<th>Description</th>
		</tr>
		<tr>
			<td>Formatting</td>
			<td>Prepares drive to hold data, can overwrite data that is already there<br/>
				The old data is still accessible through special data recovery software until overwritten</td>
		</tr>
		<tr>
			<td>Sanitation</td>
			<td>Software that thoroughly wipes data off the drive<br/>
				It can take 6 to 7 writes on a specific drive <i>sector</i> to actually overwrite the old data</td>
		</tr>
		<tr>
			<td>Destruction</td>
			<td>Renders the drive no longer operational<br/>
				Destruction may be performed in many different ways</td>
		</tr>
	</table>


