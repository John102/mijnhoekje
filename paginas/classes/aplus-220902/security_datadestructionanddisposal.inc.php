 <p class="pheader">Recycling or Repurposing: Best Practices</p>
 
 <p>Destruction is not always the way to go as a user or organization might want to reuse the media. 
 There are 2 ways of formatting a hard drive for reuse and it depends on the situation which one will be utilized.</p>
 
	<table>
		<tr>
			<th>Method</th>
			<th>Description</th>
		</tr>
		<tr>
			<td>Low level formatting</td>
			<td>This method of formatting is used to completely wipe data off the disk before reusing it</td>
		</tr>
		<tr>
			<td>Standard formatting</td>
			<td>This method marks currently occupied sectors of data as being available for data storage<br/>
				The old data actually still exists but is not 'noticed' by the OS</td>
		</tr>
	</table>
	
<p>The formatting options in the operating system are <b>always</b> a <i>standard format</i> and do not actually wipe the disk clean.</p>

 <p>A common way of <b>sanitizing</b> a hard drive is to 'Overwrite' or replace all data with 0s. (Zero Out Data) Most software will overwrite with the same sequence multiple times to ensure 
that the previous data cannot be obtained.</p>
 
 
 
 
 
 
 
 
 	


