<h3>File Management</h3>

<p class="pheader">Overview</p>

<p>All files in Windows contain certain attributes that help in detemining their purpose or assist with system activities. 
These attributes are shown in the table below.</p>

<table>
	<tr>
		<td>Archive</td>
		<td>Archive is to assist in backup operations</td>
	</tr>
	<tr>
		<td>Read-only</td>
		<td>File cannot be altered</td>
	</tr>
	<tr>
		<td>System</td>
		<td>Operating System sets this parameter to files that are part of the system</td>
	</tr>
	<tr>
		<td>Hidden</td>
		<td>Hidden means the file is hidden from default options, you can however set the folder option to 'show hidden items'</td>
	</tr>
</table>

<p>Some of these attributes can be set using the properties of the file while some must be set using the <b>attrib</b> in a command line program (command prompt or PowerShell). </p>