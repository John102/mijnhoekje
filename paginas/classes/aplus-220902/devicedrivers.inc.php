<h3>Device Drivers</h3>

<p class="pheader">Overview</p>

<p>A device driver is a small program stored on the hard drive that instructs the OS on how to communicate with a specific hardware device. 
Many drivers are either included with the default installation of Windows or obtainable through Windows Update. 
Some drivers are only available from the hardware manufacturer. You should always ensure that you trust the hardware manufacturer and that you use a device 
driver that is tested and designed for your specific operating system.</p>

<p>The primary device driver management tool available to us in Windows is called 'Device Manager'. On a Mac computer it is called 'System Information' or 'System Profiler'.</p>