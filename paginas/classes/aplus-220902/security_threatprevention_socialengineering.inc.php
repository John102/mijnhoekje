<h3>Threat Prevention: Social Engineering</h3>

<p class="pheader">Overview</p>

<p>A very common type of threat is social engineering. It comes in many forms and is applied hundreds to thousands of times every day world wide. 
The best way to prevent these threats from this type of 'hack' is through education and corporate policy.</p>

<p class="pheader">Prevention methods</p>

<p>In general, employees have to be made aware of the potential security risks involved in their daily activities aswell as their responsibilities involved in this matter. 

Some things to take into consideration when trying to prevent social engineering 'hacks' is to have a strict company policy on several aspects. 
For example, a policy on <i>how to handle mail attachments from unkown sources properly</i> will greatly reduce the risk for malicious software to be downloaded and installed on the corporate network. 
Centralized anti-virus and anti-spyware software in addition to corporate and personal firewalls will help to reduce the risk even further. 
An <i>entry control roster</i> might prevent an unwanted visitor, pretending to be an expected visitor (employee, cleaning service, etc.), from entering the corporate building.</p>

