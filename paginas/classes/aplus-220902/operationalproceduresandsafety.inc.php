<h3>Operational Procedures and Safety</h3>

<p class="pheader">Overview</p>

<p>When working with computers and technology, safety is key and should always be considered regardless of the task. 
In many cases technicians are dealing with electrical equipment and electrical safety is paramount.</p>

<p class="pheader">Electrical Safety Basics</p>

	<ul class="magiclist">
		<li>it's <b>current</b> and <b>not voltage</b> that causes danger</li>
		<li>humans can feel a single mA, 10 mA is enouth to make the muscles contract, 100mA is enough to make the heart stop</li>
		<li>human body has natural resistance to current but this can be decreased in various ways</li>
		<li>never insert anything into a wall outlet</li>
		<li>remove jewelry and other metal</li>
		<li>keep hands clean and dry</li>
		<li>stay far away from sources of moist and water</li>
	</ul>
	
<p class="pheader">ESD (Electrical Static Discharge) </p>	

<p><b>Static electricity</b> is the major concern when working with computer parts and can occur when charges on seperate objects are unequal. 
ESD (Electrical Static Discharge) has a very high risk of damaging computer internals. Therefore it is a <b>must</b> to ground yourself properly when working with 
computer internal parts. Static discharge is harmless to humans but very deadly for computer components. (high voltage, low current)</p>

<p>Static buildup can be prevented by increasing environment humidity, the use anti-static bags, wearing cotton clothing, removing carpeting.<br/>
Preventing discharge or safely guide discharges can be accomplished by staying grounded with the help of an ESD strap or ESD mat.</p>

<p class="pheader">Personal Safety</p>

	<ul class="magiclist">
		<li>always disconnect power before repairing a computer</li>
		<li>remove jewelry</li>
		<li>lifting techniques are important</li>
		<li>understanding weight limitations</li>
		<li>electrical fire safety</li>
		<li>cable management</li>
		<li>safety goggles (when needed)</li>
		<li>air filter mask (when needed; like dust)</li>
	</ul>
	
