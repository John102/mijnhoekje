<p class="pheader">MSDS (Material Safety Data Sheet)</p>

<p>In some cases you might have to deal with materials that can be toxic and harmful to humans such as cleaning solutions, paints and other chemicals. <br/>
If you interact with any of these products they will be accompanied with a MSDS that serves various purposed.

	<ul class="magiclist">
		<li>identifies important safety facts</li>
		<li>identifies safe handling and disposal</li>
		<li>describes first aid steps necessary of accidents occur</li>
		<li>describes how to store properly</li>
	</ul>

</p>

