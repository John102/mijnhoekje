<h3>Troubleshooting Mobile Devices</h3>

<p class="pheader">Overview</p>

<p>With the large number of mobile devices in todays networks, you are sure to be called upon to help assist with related problems. 
Support for tablets and smartphones will vary based on the organization but support for laptops is inevitable.</p>


<p class="pheader">Common Symptoms</p>

	<table>
		<tr>
			<th>Symptom</th>
			<th>Description</th>
		</tr>
		<tr>
			<td>dim display</td>
			<td>
				This issue might be resolved with adjusting the brightness settings
				
					<ul class="magiclist">
						<li>iOS: Settings / Display and brightness</li>
						<li>Android: Settings / Display / Brightness level</li>
						<li>Windows Phone: Settings / Brightness</li>
					</ul>
				
				If the problem remains after adjusting the brightness levels then you probably have a bad display. This display then needs to be repaired / replaced.<br/>
				Most commonly it is a backlight issue.
			
			</td>
		</tr>
		<tr>
			<td>intermittent wireless</td>
			<td>
				This issue might be resolved by moving closer to the access point or trying a different access point.
			</td>
		</tr>
		<tr>
			<td>no wireless connectivity</td>
			<td>
				Check to see if the WiFi connection is enabled on the device.<br/>
				If it is enabled, check if it is connecting to a network and if the configurations are correct (security key configuration)<br/>
				If none of the above is helping you might want to perform a hard reset to restart the wireless subsystem.
			</td>
		</tr>
		<tr>
			<td>no Bluetooth connectivity</td>
			<td>
				Check to see if the Bluetooth connection is enabled on the device.<br/>
				If it is enabled, check if the configuration and pairing process are performed correctly.<br/>
				If none of the above is helping you might want to perform a hard reset to restart the Bluetooth subsystem.
			</td>
		</tr>
		<tr>
			<td>cannot broadcast to external monitor</td>
			<td>Broadcasting to a TV is commonly used with Apple TV, Xbox, Playstation, Chromecast, Roku, Smart TVs. Since every broadcast device is different, they all have their own 
				requirements. If the broadcasting is not working; check the application requirements.<br/>
				Most of these configurations require both of the devices to be on the same wireless network.<br/>
				Signal strength is also very important; between phone and television and television and the Internet and ofcourse phone and the Internet</td>
		</tr>
		<tr>
			<td>touchscreen non-responsive</td>
			<td>When the touchscreen is completely black or touchscreen is not responding to button or screen press input it might sometimes still restart by utilizing the power button on the device.<br/>
				<ul class="magiclist">
					<li>Apple iOS restart: Press power button, slide to power off, press powerbutton</li>
					<li>Apple iOS restart: Hold down power button and Home button for 10 seconds to restart</li>
					<li>Android restart: Remove the battery, put it back in and power on</li>
					<li>Android restart: Hold down power and volume down until device restarts (Some phones have different key combinations and some do not have a key-based reset)</li>
				</ul>

			</td>
		</tr>
		<tr>
			<td>apps not loading / poor performance</td>
			<td>
				In this case restarting the phone might fix the issue.<br/>
				If the application itself is running slow you might have to stop and restart the application itself.
				
					<ul class="magiclist">
						<li>iPhone: Double-tap home button, slide app up</li>
						<li>Android: Settings/Apps, select app, Force stop</li>
					</ul>
			</td>
		</tr>
		<tr>
			<td>unable to decrypt email</td>
			<td>Email encryption is a secure way to sending and receiving mail. It is built-in to corporate email systems like <i>MS Outlook</i>.<br/>
				Every single user has a private key for decryption, you can't decrypt without this key.<br/>
				Install individual private keys on every mobile device by using a Mobile Device Manager (MDM).</td>
		</tr>
		<tr>
			<td>extremely short battery lifespan</td>
			<td>This might be caused due to a bad reception. If your phone is always searching for a decent signal and has a hard time finding one, it will cost battery life.<br/>
				In this case you can turn on Airplane mode so that your phone will stop forcing these signal connections in order to safe battery. However, in Airplane mode you're not connected to 
				your mobile service provider and therefore cannot be called or texted at that time.<br/>
				Disabling other unnecessary features will also increase your phones battery life span. Features like <i>802.11 Wireless (WiFi), Bluetooth and GPS</i> can be turned off when not used.<br/>
				Your mobile device also allows for application battery usage checking. This will show you exactly how much battery drain comes with every active application.<br/>
				
					<ul class="magiclist">
						<li>iOS: Settings/General/Usage</li>
						<li>Android: Settings/Battery</li>
					</ul>
				
				Aged batteries also perform less than new batteries. There is only so many recharges in a battery lifespan. Batteries will decrease in holding capacity over time and usage. 
				When the battery reaches the end of its lifespan it is time to replace it.
				</td>
		</tr>
		<tr>
			<td>overheating</td>
			<td>The device will automatically turn itself off when it's getting too hot. Factors that will increase device heat are
				
				<ul class="magiclist">
					<li>charging / discharging</li>
					<li>cpu usage</li>
					<li>display light</li>
				</ul>
				
				You can check application resource usage to find out if the heating is caused by an application. This application will use a lot of CPU power in order to perform.
				
				
			</td>
		</tr>
		<tr>
			<td>frozen system</td>
			<td>When the device does not respond to screen or button input you may want to perform a soft reset.
				Hold down the power button and turn off the device. If this does not work you might want to perform a hard reset.<br/>
				<ul class="magiclist">
					<li>Apple iOS restart: Hold down power button and Home button for 10 seconds to restart</li>
					<li>Android restart: Remove the battery, put it back in and power on</li>
					<li>Android restart: Hold down power and volume down until device restarts (Some phones have different key combinations and some do not have a key-based reset)</li>
				</ul>
				
				If the system freezing problem continues you might want to install the latest operating system and reload by performing a <b>factory reset</b>.
			</td>
		</tr>
		<tr>
			<td>no sound from speakers</td>
			<td>Check the volume settings in the application that you expect sound from and in the phone settings itself.<br/>
				If that was not the issue you might be dealing with bad software. Try to delete and reload the application.<br/>
				A third option that might work is to use a headphone because the internal speaker of your device might be broken.<br/>
				When the sound starts and then stops it might be that there are 'dueling applications' that both produce sound.<br/>
				Keep the application that you want to sound from on the foreground of the device.<br/>
				
				The last solution is to load the latest software or perform a factory reset.
				
			</td>
		</tr>
		<tr>
			<td>inaccurate/unresponsive touchscreen</td>
			<td>Try closing some active applications to lower the memory usage and release resource contention.<br/>
				If this does not help you might want to restart your device with a soft reset unless a hard reset is necessary.<br/>
				In the worst case it requires a hardware fix (replacing the digitizer or reseat the cables)
				</td>
		</tr>
		<tr>
			<td>system lockout</td>
			<td>Too many incorrect unlock attempts might lock you out of the device.<br/>
			
				<ul class="magiclist">			
					<li>iOS: device might be configured to erase the phone after 10 failed attempts (This setting can be disabled however, this increases a retry delay with failed attempts)</li>
					<li>Android: locks and wipes the phone after failed attempts but you can use a Google login to unlock the phone</li>
					<li>Windows Phone: locks after failed attempts and may require a hard reset or factory reset</li>
				</ul>
			</td>
		</tr>
	</table>

	
	
	
	
	
	
	
	
	
	
	
	