<h3>Power Issues</h3>

<p class="pheader">Overview</p>

<table>
	<tr>
		<th>Issue</th>
		<th>Description</th>
	</tr>
	<tr>
		<td>power surge</td>
		<td>a sudden increase of power that can damage electrical equipment if not connected to a working surge protector</td>
	</tr>
	<tr>
		<td>brownout</td>
		<td>a decrease in power that does not result in a complete outage but could cause systems to reboot if not connected to a battery backup</td>
	</tr>
	<tr>
		<td>blackout</td>
		<td>complete and prolonged loss in power</td>
	</tr>
</table>

<p>There are some methods and equipment to protect systems and other electrical equipment from these issues. <br/>
A <b>UPS</b> (Uninterruptible Power Supply) makes sure that the equipment continues to run properly (as long as the battery lasts) when a brownout or blackout occur.<br/>
There are also <b>surge protectors</b> available to protect electrical equipment from power surges.</p>