<h3>Windows PowerShell</h3>

<p class="pheader">Overview</p>

<p>Windows PowerShell is a command line administrative interface designed with system administration in mind. 
It is a powerful scripting tool used to perform virtually any task. <br />
Included in Windows Vista and beyond with incremental versions and capabilities.<br/>
PowerShell 4.0 ships with Windows 8.1.<br />
Familiar syntax to all 'cmdlets'. ( <i>verb-noun -Parameter Value</i>)<br />

	<p class="code">New-NetIPAddress -InterfaceAlias "Local Area Connection" -IPAddress 192.168.1.100</p>

</p>