<img class="image-block" src="<?=$class_220902?>multimedia/linux.png" />

<p class="pheader">Overview</p>

<p>The Linux OS is an open source operating system. This means that the kernel source code is freely available on the Internet. 
There are many distributions of Linux available, all optimized for different purposes. Allthough Linux is open source, 
there are still distributions out there that have to be paid for. This is due to the fact that these distributions are custom built 
by people or a company and they simple want to get paid for it. This option will still be a lot cheaper than buying Windows or OS X in many cases.</p>

