<h3>Internet Appliances</h3>

<p class="pheader">Overview</p>

<p>Internet appliances are devices that are dedicated to provide a particular set of functionality. In many cases they can perform better than a server 
that is utilizing software roles in order to achieve the same goals. These, often physical, devices are referred to as Internet appliances.</p>


<p class="pheader">Unified Threat Management (UTM)</p>

<p>This is an approach to providing multiple security functions within the same device or appliance. Functions may include but are not restricted to: 
	<ul class="magiclist">
		<li>Firewall</li>
		<li>Intrusion prevention</li>
		<li>Gateway antivirus and antispam</li>
		<li>VPN</li>
		<li>Content filtering</li>
		<li>Load balancing</li>
		<li>Data loss prevention</li>
		<li>On application reporting</li>
	</ul>
</p>

<p class="pheader">Intrusion Detection System (IDS)</p>

<p>This system is responsible for detection unauthorized access or attacks. Verifies, itemizes and characterizes threats internally and externally.<br/>
Provides event notifications and alerts. Network based (NIDS) are the most common monitoring traffic on the network segment and requiring a NIC operating 
in promiscuous mode.</p>

<p class="pheader">Intrusion Prevention System (IPS)</p>

<p>This device scans traffic on a network for signs of malicious activity. Destinguished from IDS in its ability to respond to network threats as opposed to just 
reporting them. Must be carefully configured to prevent <i>false positives and false negatives</i>.<br/>
<b>False positive</b> - The IPS detects malicious activity while in fact it is not malicious.</br>
<b>False negative</b> - The IPS ignores malicious activity because it does not recognize the macilious behavior / properties.</p>

<p class="pheader">Embedded System</p>

<p>A type of operating system with a specific function within a larger computing system. These are present on many internet-connected devices such as VoIP phones, routers and 
they are also increasingly found on home appliances and automobiles.</p>

<p class="pheader">Legacy Systems</p>

<p>These are systems that have essentially been handed down from one version of a system to another without any major revisions.</p>