<h3>Communication and Professionalism</h3>

<p class="pheader">Overview</p>

<p>PC technicians interact with people constantly and in many adverse situations so professionalism and customer service concepts are very 
important to ensure success. Professional communicators must realize the necessity of being professional and courteous at all times in addition to a number of 
other traits.


	<table>
		<tr>
			<th>Competency</th>
			<th>Description</th>
		</tr>
		<tr>
			<td>consider the total message</td>
			<td>
			
				<ul class="magiclist">
					<li>the message is more than just your words</li>
					<li>facial expressions and body posture</li>
					<li>tone of voice</li>
					<li>maintain a positive attitude</li>
					<li>project confidence but not arrogance</li>
				</ul>
			
			</td>
		</tr>
		<tr>
			<td>stay focused and actively listen</td>
			<td>
			
				<ul class="magiclist">
					<li>don't waste time with idle chit chat</li>
					<li>most customers are paying for time</li>
					<li>fix the problem and move on</li>
					<li>can be friendly and engaging in certain situations</li>
					<li>avoid distractions like personal calls, texting, social media, talking to co-workers, personal interruptions, etc.</li>
					<li>avoid topics such as politics and religion</li>
					<li>actively listen (take notes) and avoid interrupting the customer</li>
				</ul>
			
			</td>
		</tr>
		<tr>
			<td>consider the customers competence</td>
			<td>
			
				<ul class="magiclist">
					<li>match your communication level with the customer's ability</li>
					<li>keep in mind that many people will overstate their understanding</li>
					<li>avoid using jargon where plain language suffices</li>
					<li>consider the customer's sense of pride in the fact that they had to call for help though they consider themselves tech savvy</li>
				</ul>
				
				<p>Sometimes users are trying to hide certain embarassing actions.</p>
				
				<ul class="magiclist">
					<li>uses may claim to have already performed certain troubleshooting actions</li>
					<li>always calmly tell the user that you are required to go through certain steps to isolate the problem</li>
					<li>always be clear in helping the user so that the problem does not reoccur</li>
				</ul>		
			
			</td>
		</tr>
		<tr>
			<td>respect the customer</td>
			<td>
			
				<ul class="magiclist">
					<li>be on time</li>
					<li>don't minimize the problem</li>
					<li>avoid distractions such as personal phone calls, text messaging or web browsing</li>
					<li>don't eat or drink in the customer's space</li>
					<li>don't use the printer, fax machine or phones unless it's part of the troubleshooting efforts</li>
					<li>try to avoid adjusting monitor, keyboard and chair locations</li>
					<li>respect the customers privacy</li>
					<li>never interrupt the customer or argue with them</li>
					<li>never discuss or distribute any information overheard at the customer location</li>
					<li>deal appropriately with a customers confidential and private information located on a computer, desktop, printer, etc.</li>
				</ul>
			
			</td>
		</tr>
		<tr>
			<td>be culturally sensitive</td>
			<td>
			
				<ul class="magiclist">
					<li>begins with knowledge about different cultures</li>
					<li>help to identify differences without passing judgment on them</li>
					<li>request training if constantly dealing with customers from a different cultural background</li>
				</ul>
			
			
			</td>
		</tr>
		<tr>
			<td>match the delivery channel to the customer</td>
			<td>
			
				<ul class="magiclist">
					<li>make sure the communication channel you use to communicate is the one the customer is most comfortable with</li>
				</ul>
			
			
			</td>
		</tr>
		<tr>
			<td>deal with difficult customers and/or situations appropriately</td>
			<td>
			
				<ul class="magiclist">
					<li>it's not always going to be easy because customers are sometimes going to be stressed and frantic when they have a computer issue</li>
					<li>avoid arguing with the customer</li>
					<li>avoid getting defensive</li>
					<li>avoid dismissing the customers problems</li>
					<li>avoid being judgmental</li>
					<li>always clarify customer statements using open ended questions to narrow the scope of the problem, restate the issue or question to verify understanding</li>
					<li>don't disclose your experience via social media outlets</li>
				</ul>
			
			</td>
		</tr>
	</table>
</p>


<p class="pheader">Set and meet expectations</p>

<p>

	<ul class="magiclist">
		<li>set reasonable timelines</li>
		<li>communicate status as appropriate with the customer</li>
		<li>offer different repair or replacement options if applicable and thoroughly explain the options and their consequences</li>
		<li>provide proper documentation on the services provided</li>
		<li>always follow up with the customer/user at a later date to verify satisfaction</li>
	</ul>

</p>



<p class="pheader">Guidelines for effective communication</p>

<p>

	<ul class="magiclist">
		<li>speak clearly</li>
		<li>avoid jargon</li>
		<li>keep messages concise and specific</li>
		<li>make sure the message is understood</li>
		<li>listen actively</li>
		<li>paraphrase messages to ensure understanding</li>
	</ul>



</p>


















