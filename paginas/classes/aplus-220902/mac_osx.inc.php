<h3>OS X (MacOS)</h3>

<img src="<?=$class_220902?>multimedia/osx.png" />

<p class="pheader">Overview</p>

<p>Since 2001 the operating system for Apple computers is called OS X. This OS is the proprietary operating system for Macintosh computers.
However, OS X allows for Windows to run in a virtual machine. The OS is very user-friendly and commonly used on systems that are meant for designing and editing.</p>


