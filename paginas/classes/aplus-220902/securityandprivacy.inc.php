<h3>Security and Privacy Guidelines for Troubleshooting</h3>

<p class="pheader">Overview</p>

<p>Working in the IT profession, it is quite possible that you will encounter situations in which you have proof of users engaging in prohibited activities. 
	
	<ul class="magiclist">
		<li>company policy infringement</li>
		<li>data leakage</li>
		<li>violation of state and federal laws</li>
	</ul>
	
It is the responsibility of the IT professional to respond appropriately and accordingly if/when these situations arise regardless of whether or not you agree with the 
prohibition. If rules are violated you must respond in a professional and legal manner.

</p>

<p>In a vast majority of cases, the extent to which a security event causes harm to the organization depends largely on the speed and quality of the initial response 
to the event. For this reason most organizations will develop a structured <b>incedent response policy</b> (IRP).
	
	<ul class="magiclist">
		<li>this minimizes the chances of extensive damage</li>
		<li>increases organizational ability of bringing those involved in illegal activities to justice through prosecution</li>		
	</ul>

</p>

<p class="pheader">Incedent Response Guidelines</p>

<p>First response clearly defines activities that are related to the initial point when the security incident is realized and must be responsed to.<br/>


	<table>
		<tr>
			<th>step</th>
			<th>description</th>
		</tr>
		<tr>
			<td>identify problem</td>
			<td>
				involved identifying what policy or law prohibits the action that was taken<br/>
				prohibited content will generally fall into certain categories
				
					<ul class="magiclist">
						<li>exploiting people</li>
						<li>promoting harassment of any person or group</li>
						<li>containing or promoting illegal or unauthorized material</li>
						<li>promoting racism, hatred, bigotry or physical harm</li>
						<li>containing adult content</li>
						<li>violating privacy rights, copyrights, contracts, etc.</li>
						<li>viruses and malware of any sort</li>
						<li>impersonation</li>
						<li>soliciting information from minors</li>
						<li>involving pyramid schemes, junk mail, chain letters, spamming, etc.</li>
					</ul>
				
			</td>
		</tr>
		<tr>
			<td>report through proper channels</td>
			<td>
				once identified, the problem must be reported through the proper channels to the appropriate individuals
					
					<ul class="magiclist">
						<li>violations of company policy will likely utilize human resource</li>
						<li>violations of state and federal law would involve law enforcement</li>
					</ul>
					
				carefully determine whether or not to involve law enforcement
					
			</td>
		</tr>
		<tr>
			<td>preserve data</td>
			<td>
				content you discover must be properly preserved until such time as it can be turned over to the correct authority
				
					<ul class="magiclist">
						<li>could require the commandeering of anything from flash drives, laptops, mobile devices and even network servers</li>
						<li>data must be preserved in the state you discovered it until you are relieved by someone in a position of authority</li>
					</ul>
			
				knowning exactly what to do in every situation may not come naturally so you should simply follow the procedure outlines in the incident response plan (IRP)
				
			</td>
		</tr>
	</table>

	
<p>Another important aspect of response policy is the use of documentation and documentation changes. During the entire process you need to identify, detect and report the problem. 
This information is valuable if the problem escalates to the judicial system.</p>
	

<p class="pheader">Chain of Custody</p>

<p>When you begin to collect evidence you must keep track of it at all times.

	<ul class="magiclist">
		<li>who was it</li>
		<li>who has seen it</li>
		<li>where has it been</li>
	</ul>
	
Sometimes chain of custody forms are required.
</p>



<p class="pheader">Licensing / DRM / EULA</p>

<p>While we might want software, music and videos to be free, this is not the case. Use of these items without paying for them is illegal by law.<br/>
Most software including operating systems and applications require that you accept an <i>End User License Agreement</i>.<br/>
Digital media is protected by <i>Digital Rights Management</i> (DRM).<br/>
Not all software requires a license and there are different types of licenses.

</p>


<p class="pheader">Open Source vs. Commercial License</p>

<p>
	<ul class="magiclist">
		<li>open source software is free and available to all</li>
		<li>commercial software requires the purchase of a license in order to legally use it</li>
		<li>commercial software often provides better support and is more well known to users</li>
	</ul>
</p>


<p class="pheader">Personal License vs. Enterprise License</p>

<p>
	<ul class="magiclist">
		<li>individual licenses allow a single user to install and use a piece of software</li>
		<li>enterprise licenses will provide installation on multiple devices</li>
	</ul>
</p>

<p class="pheader">Personally Identifiable Information (PII)</p>

<p>Any piece of information about a user that can be used alone or in combination with other information to identify that person.<br/>
There is always a danger for companies in leaking PII.<br/>
Whatever you are doing, always follow corporate end-user policies and security best practices.</p>






















